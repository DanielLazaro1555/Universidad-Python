# Operadores Aritmeticos

a = 10
b = 3

# Suma +

suma = a+b
print(f'Suma: {suma}')

# Resta -
resta = a-b
print(f'Resta: {resta}')

# Multiplicacion *
multiplica = a*b
print(f'Multiplicacion: {multiplica}')

# Division /
division = a/b
print(f'Division: {division:.2f}')

# Division Entera (//)
division_entera = a//b
print(f'Division Entera: {division_entera}')

# Modulo (%) residuo de la division
modulo = a % b
print(f'Residuo de la division: {modulo}')

# Exponente(potencia) **
exponente = a ** b  # 10 elevado a la 3 = 10*10*10=1000
print(f'Exponente: {exponente}')

# Metodos de cadenas

cadena1 = 'Hola Mundo'
print(f'Cadena Original: {cadena1}')

mayuscula = cadena1.upper()
print(f'Cadena en Mayuscula: {mayuscula}')

print(f'Cadena en Minuscula: {cadena1.lower()}')

cadena2 = ' Juan Perez '
print(f'Cadena con espacios: {cadena2}')
# Elimina espacios al inicio y al final
print(f'Cadena sin espacios: {cadena2.strip()}')

# Operacion clasica de asignacion
miVariable = 10
print(miVariable)
# Operacion de adicion a la asignacion
miVariable = miVariable + 1
print(miVariable)
# Incremento con reasignacion
miVariable += 1
print(miVariable)
# Decremento con reasignacion
miVariable -= 2
print(miVariable)
# Multiplicacion con reasignacion
miVariable *= 3
print(miVariable)
# Division con reasignacion
miVariable /= 2
print(miVariable)
# Ejemplo de tipos de datos en python

# Entero
edad = 30
print('Edad:', edad)

# Numero con punto flotante
altura = 1.75
print('Altura:', altura)

# Cadena de texto
nombre = 'Ana'
print('Nombre:', nombre)

# Tipo boolano
es_estudiante = False
print('¿Es estudiante?:', es_estudiante)

# None, ausencia de valor
direccion = None
print('Dirección:', direccion)
